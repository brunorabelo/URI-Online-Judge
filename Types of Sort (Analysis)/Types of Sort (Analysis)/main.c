void bubleSort(int *vector,int length){
    int i,j;
    int temp;
    
    for(i=0;i<length;i++){
        for(j=length-1;j>i;j--){
            if(vector[j-1]>vector[j]){
                temp=vector[j-1];
                vector[j-1]=vector[j];
                vector[j]=temp;
            }
        }
    }
}

